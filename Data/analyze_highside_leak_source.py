HW = np.array([bin(i).count("1") for i in range(256)], dtype=np.float64)
RCON = np.array([0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1B, 0x36], dtype=np.uint8)


def aes128_round10_key(master_key: np.ndarray) -> np.ndarray:
    w = np.zeros((44, 4), dtype=np.uint8)
    for i in range(4):
        w[i] = master_key[4 * i: 4 * i + 4]
    for i in range(4, 44):
        t = w[i - 1].copy()
        if i % 4 == 0:
            t = np.roll(t, -1)
            t = SBOX[t]
            t[0] ^= RCON[(i // 4) - 1]
        w[i] = np.bitwise_xor(w[i - 4], t)
    return w[40:44].reshape(16)


def preprocess(traces: np.ndarray, mode: str) -> np.ndarray:
    if mode == "none":
        return traces.astype(np.float64, copy=False)
    t = traces.astype(np.float64, copy=False)
    t = t - t.mean(axis=1, keepdims=True)
    x = np.linspace(-1.0, 1.0, t.shape[1], dtype=np.float64)
    t = t - np.outer((t @ x) / np.dot(x, x), x)
    return t


def corr_curve(tc: np.ndarray, tstd: np.ndarray, h: np.ndarray) -> np.ndarray:
    hc = h.astype(np.float64) - np.mean(h)
    hstd = np.std(hc, ddof=1) + 1e-15
    return (hc @ tc) / ((tc.shape[0] - 1) * hstd * tstd)


def model_value(name: str, ptb: np.ndarray, ctb: np.ndarray, k0b: int, k10b: int) -> np.ndarray:
    x0 = np.bitwise_xor(ptb, np.uint8(k0b))
    x10 = np.bitwise_xor(ctb, np.uint8(k10b))
    if name == "hw_pt":
        return HW[ptb]
    if name == "hw_pt_xor_k0":
        return HW[x0]
    if name == "hw_sbox_pt_xor_k0":
        return HW[SBOX[x0]]
    if name == "hd_ptxor_to_sbox":
        return HW[np.bitwise_xor(x0, SBOX[x0])]
    if name == "hw_ct":
        return HW[ctb]
    if name == "hw_ct_xor_k10":
        return HW[x10]
    if name == "hw_invsbox_ct_xor_k10":
        return HW[INV_SBOX[x10]]
    if name == "hd_invsbox_to_x10":
        return HW[np.bitwise_xor(INV_SBOX[x10], x10)]
    raise ValueError(name)


def main() -> None:
    ap = argparse.ArgumentParser(description="Known-key leakage source attribution on highside dataset")
    ap.add_argument("--npz", default="dataset_aes_sca_highside.npz")
    ap.add_argument("--n-traces", type=int, default=1000)
    ap.add_argument("--out", default="highside_leak_source_report.json")
    args = ap.parse_args()

    d = np.load(args.npz)
    required = {"traces", "traces_a", "traces_b", "plaintexts", "ciphertexts", "key"}
    missing = required.difference(set(d.files))
    if missing:
        raise ValueError(f"Missing fields in dataset: {sorted(missing)}")

    n = min(args.n_traces, d["traces"].shape[0])
    pt = d["plaintexts"][:n, :16].astype(np.uint8)
    ct = d["ciphertexts"][:n, :16].astype(np.uint8)
    k0 = d["key"][:16].astype(np.uint8)
    k10 = aes128_round10_key(k0)

    sources = {
        "traces": d["traces"][:n].astype(np.float32),
        "traces_a": d["traces_a"][:n].astype(np.float32),
        "traces_b": d["traces_b"][:n].astype(np.float32),
    }
    models = [
        "hw_pt",
        "hw_pt_xor_k0",
        "hw_sbox_pt_xor_k0",
        "hd_ptxor_to_sbox",
        "hw_ct",
        "hw_ct_xor_k10",
        "hw_invsbox_ct_xor_k10",
        "hd_invsbox_to_x10",
    ]

    rows = []
    for src_name, src in sources.items():
        for preproc in ["none", "center_detrend"]:
            tr = preprocess(src, preproc)
            tc = tr - tr.mean(axis=0, keepdims=True)
            tstd = tc.std(axis=0, ddof=1) + 1e-15
            for model in models:
                best_abs = -1.0
                best_byte = -1
                best_poi = -1
                for b in range(16):
                    h = model_value(model, pt[:, b], ct[:, b], int(k0[b]), int(k10[b]))
                    c = corr_curve(tc, tstd, h)
                    ac = np.abs(c)
                    poi = int(np.argmax(ac))
                    score = float(ac[poi])
                    if score > best_abs:
                        best_abs = score
                        best_byte = b
                        best_poi = poi
                rows.append(
                    {
                        "source": src_name,
                        "preproc": preproc,
                        "model": model,
                        "best_abs_corr": best_abs,
                        "best_byte": best_byte,
                        "best_poi": best_poi,
                    }
                )
                print(
                    f"[{src_name:8s}|{preproc:13s}|{model:23s}] "
                    f"corr={best_abs:.5f} byte={best_byte:2d} poi={best_poi}"
                )

    rows_sorted = sorted(rows, key=lambda r: r["best_abs_corr"], reverse=True)
    out = {
        "npz": args.npz,
        "n_traces": n,
        "master_key_hex": "".join(f"{x:02X}" for x in k0),
        "round10_key_hex": "".join(f"{x:02X}" for x in k10),
        "all_results": rows,
        "top10": rows_sorted[:10],
        "best_overall": rows_sorted[0],
    }
    Path(args.out).write_text(json.dumps(out, indent=2))
    print("best_overall:", rows_sorted[0])
    print("saved:", args.out)


if __name__ == "__main__":
    main()
