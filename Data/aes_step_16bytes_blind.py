HW = np.array([bin(i).count("1") for i in range(256)], dtype=np.float64)


def preprocess(tr):
    t = tr.astype(np.float64, copy=False)
    t = t - t.mean(axis=1, keepdims=True)
    x = np.linspace(-1.0, 1.0, t.shape[1], dtype=np.float64)
    t = t - np.outer((t @ x) / np.dot(x, x), x)
    return t


def score_key_trace(tr, pbyte, key):
    tc = tr - tr.mean(axis=0, keepdims=True)
    tstd = tr.std(axis=0, ddof=1) + 1e-15
    h = HW[SBOX[np.bitwise_xor(pbyte, key)]]
    hc = h - h.mean()
    hstd = h.std(ddof=1) + 1e-15
    return (hc @ tc) / ((tr.shape[0] - 1) * hstd * tstd)


def main():
    ap = argparse.ArgumentParser(description="AES-step blind fullscan CPA (no true key usage)")
    ap.add_argument("--npz", required=True)
    ap.add_argument("--n-traces", type=int, default=20000)
    ap.add_argument("--win-start", type=int, default=0)
    ap.add_argument("--win-end", type=int, default=3968)
    ap.add_argument("--out-prefix", default="aes_step_16b_blind")
    args = ap.parse_args()

    d = np.load(args.npz)
    tr = d["traces"][:args.n_traces, args.win_start:args.win_end]
    pt = d["plaintexts"][:args.n_traces, :16].astype(np.uint8)
    tr = preprocess(tr)

    recovered = []
    rows = []
    for b in range(16):
        scores = np.zeros(256, dtype=np.float64)
        best_poi = np.zeros(256, dtype=np.int32)
        for k in range(256):
            c = score_key_trace(tr, pt[:, b], k)
            p = int(np.argmax(np.abs(c)))
            scores[k] = float(np.abs(c[p]))
            best_poi[k] = p
        order = np.argsort(scores)[::-1]
        k1, k2 = int(order[0]), int(order[1])
        recovered.append(k1)
        poi = int(best_poi[k1]) + args.win_start
        margin = float(scores[k1] - scores[k2])
        print(f"byte{b:02d}: top=0x{k1:02X} score={scores[k1]:.6f} poi={poi} margin={margin:.6f}")
        rows.append(
            {
                "byte": b,
                "top_key": k1,
                "top_key_hex": f"{k1:02X}",
                "score": float(scores[k1]),
                "poi": poi,
                "margin_top1_top2": margin,
                "top5": [{"key": int(k), "score": float(scores[k])} for k in order[:5]],
            }
        )

    key_hex = "".join(f"{k:02X}" for k in recovered)
    print("Recovered key (hex):", key_hex)
    print("Recovered key (C):", ",".join(f"0x{k:02X}" for k in recovered))

    out = {
        "npz": args.npz,
        "n_traces": int(args.n_traces),
        "window": [int(args.win_start), int(args.win_end)],
        "recovered_key_hex": key_hex,
        "recovered_key": [int(x) for x in recovered],
        "bytes": rows,
        "blind": True,
    }
    out_json = f"{args.out_prefix}_summary.json"
    with open(out_json, "w") as f:
        json.dump(out, f, indent=2)
    print("saved:", out_json)


if __name__ == "__main__":
    main()
