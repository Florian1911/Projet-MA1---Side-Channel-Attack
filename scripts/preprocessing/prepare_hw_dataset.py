HW = np.array([bin(i).count("1") for i in range(256)], dtype=np.uint8)


def center_and_detrend(traces: np.ndarray) -> np.ndarray:
    t = traces.astype(np.float64, copy=False)
    t = t - t.mean(axis=1, keepdims=True)
    x = np.linspace(-1.0, 1.0, t.shape[1], dtype=np.float64)
    x2 = float(np.dot(x, x))
    slopes = (t @ x) / x2
    t = t - np.outer(slopes, x)
    return t.astype(np.float32)


def snr_curve(traces: np.ndarray, classes: np.ndarray, n_classes: int) -> np.ndarray:
    means = np.zeros((n_classes, traces.shape[1]), dtype=np.float64)
    vars_ = np.zeros((n_classes, traces.shape[1]), dtype=np.float64)
    counts = np.zeros((n_classes,), dtype=np.int64)
    for c in range(n_classes):
        idx = (classes == c)
        nc = int(idx.sum())
        if nc == 0:
            continue
        counts[c] = nc
        tc = traces[idx]
        means[c] = tc.mean(axis=0)
        vars_[c] = tc.var(axis=0)
    valid = counts > 0
    m = means[valid]
    v = vars_[valid]
    return m.var(axis=0) / (v.mean(axis=0) + 1e-12)


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--in", dest="inp", default="dataset_20k.npz")
    ap.add_argument("--out", dest="out", default="dataset_20k_hw.npz")
    ap.add_argument("--byte", type=int, default=0)
    ap.add_argument("--key", type=lambda x: int(x, 0), default=0x2B)
    ap.add_argument("--center", type=int, default=-1, help="window center; -1=auto via SNR")
    ap.add_argument("--window", type=int, default=200)
    args = ap.parse_args()

    d = np.load(args.inp)
    traces = d["traces"].astype(np.float32)
    plains = d["plaintexts"].astype(np.uint8)
    key = d["key"] if "key" in d.files else np.array([args.key], dtype=np.uint8)
    over = d["overflows"] if "overflows" in d.files else None

    inter = AES_SBOX[np.bitwise_xor(plains[:, args.byte], args.key)]
    labels_hw = HW[inter].astype(np.uint8)

    proc = center_and_detrend(traces)
    snr = snr_curve(proc, labels_hw, 9)
    center = int(np.argmax(snr)) if args.center < 0 else int(args.center)
    half = args.window // 2
    w0 = max(0, center - half)
    w1 = min(proc.shape[1], center + half)
    traces_crop = traces[:, w0:w1]
    proc_crop = proc[:, w0:w1]

    np.savez(
        args.out,
        traces=traces_crop,
        traces_proc=proc_crop,
        plaintexts=plains,
        labels=labels_hw,
        key=key,
        overflows=over if over is not None else np.array([], dtype=np.int16),
        window_start=np.int32(w0),
        window_end=np.int32(w1),
        window_center=np.int32(center),
        window_len=np.int32(w1 - w0),
        byte_idx=np.int32(args.byte),
    )
    print(f"saved: {args.out} | traces={traces_crop.shape} | center={center} | window=[{w0}:{w1}]")

    meta = {
        "input": args.inp,
        "output": args.out,
        "byte": int(args.byte),
        "key": int(args.key),
        "window_start": int(w0),
        "window_end": int(w1),
        "window_center": int(center),
        "window_len": int(w1 - w0),
        "snr_max": float(snr[center]),
    }
    meta_path = Path(args.out).with_suffix(".json")
    with meta_path.open("w") as f:
        json.dump(meta, f, indent=2)
    print(f"saved: {meta_path}")


if __name__ == "__main__":
    main()
